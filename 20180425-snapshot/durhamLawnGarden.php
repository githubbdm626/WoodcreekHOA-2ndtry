<h1>Durham Lawn and Garden Resources</h1>
Here are some resources that may be helpful (or at least interesting) for your outdoor efforts.

<Br><br>


<b>Lawn</b>
<ul>
<li><a href="http://www.turffiles.ncsu.edu/">NC State Turffiles</a> - There's really one authority when it comes to lawn maintenance in North Carolina and that's NC State. This website is a fantastic resource for any type of grass in NC.</li>
<li><a href="http://www.turffiles.ncsu.edu/Guides.aspx#004175">Carolina Lawns</a> - The flagship publication of NC State Turfgrass Center is a hefty document called "Carolina Lawns." You can get it at that link.
<li><a href="http://woodcreeknc.org/resources/Tall_Fescue.pdf">Fescue Lawn Maintenance</a> - Here's 2-page document with a maintenance schedule for Tall Fescue, which is the turf variety originally planted in Woodcreek.</li>


</ul>
<b>Garden</b>

<ul>
<li><a href="http://attra.ncat.org/attra-pub/farmscape.html">Farmscaping to Enhance Biological Control</a> - managing biodiversity on a farm to favor beneficial organisms. </li>
<li><a href="http://attra.ncat.org/attra-pub/complant.html">Companion Planting</a> - certain plants can benefit others when planted in near proximity.</li>
<li><a href="http://attra.ncat.org/attra-pub/squash_pest.html">Squash Bug and Squash Vine Borer</a> - Squash bug and squash vine borer are major pests among cucurbits. This publication addresses organic control methods for these pests.</li>
<li><a href="http://www.dpughphoto.com/true_bugs.htm">Durham Bug Photos</a> - contains many photos of insects taken in Durham NC.</li>
<li><a href="http://www.ces.ncsu.edu/depts/hort/consumer/factsheets/">Plant Fact Sheets</a> - Information about and photos of plants specific to North Carolina</a></li>


</ul>



