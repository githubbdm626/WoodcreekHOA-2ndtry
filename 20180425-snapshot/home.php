<h1>New to Woodcreek?</h2>
<img src="accents/inline4.JPG" class="inline" style="float:right">

 Woodcreek is a small, friendly community of single-family homes in southeast Durham, North Carolina. We are located just east of Barbee Rd, convenient to Duke, UNC, Research Triangle Park, Southpoint, and RDU airport. Residents of Woodcreek are a diverse group including students, young families, empty nesters, and retirees. You can read more on our <a href="index.php?action=about">About Woodcreek</a> page, or if you thinking about moving in, be sure to read <a href="index.php?action=prospective">Why Choose Woodcreek?</a> and check out some <a href="index.php?action=photos">photos</a>! Woodcreek residents also utilize the website <a href="https://nextdoor.com/neighborhood/woodcreekdurham--durham--nc/">Nextdoor</a> for sharing community-related information.
