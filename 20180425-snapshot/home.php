		  <h1>Woodcreek HOA</h2>
	The Woodcreek Homeowners Association exists to:
	<ul>
		<li>protect and increase property value</li> 
		<li>hear and address neighborhood concerns</li>   
		<li>maintain and improve common areas</li> 		  
		<li>organize and facilitate community events and meetings</li> 		  
	</ul>
	In these efforts, we endeavor to enforce community <a href="http://www.woodcreeknc.org/resources/WCHandbook.doc">covenants and bylaws (.doc)</a> in a responsible and understanding manner. 


